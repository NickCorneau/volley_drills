/**
 * Diagnostic-spine dimensions.
 *
 * **Role (post-2026-05-07 D138 resolution).** This module owns the
 * shared dimension constants and their types for the live generated-
 * plan diagnostic (`app/src/domain/generatedPlanDiagnostics.ts`). It
 * intentionally does NOT own a coverage audit anymore ‚Äî the catalog-
 * coverage detection diagnostic is `app/src/data/focusCoverageAudit.ts`,
 * which is the canonical surface (snapshot regression test in
 * `app/src/data/__tests__/focusCoverageAudit.test.ts`, markdown report
 * generated by `app/scripts/generate-coverage-report.ts` and committed
 * at `docs/reviews/2026-05-04-focus-coverage-audit.md`).
 *
 * The pre-2026-05-07 file also exposed `buildFocusReadinessAudit`,
 * `evaluateFocusReadinessCell`, a gap-card / activation-manifest
 * remediation API (`FocusReadinessGapCard`, `ActivationBatchManifest`,
 * `validateFocusReadinessGapCard`, `validateActivationBatchManifest`,
 * `canTransitionReadinessStatus`, `isReadinessStatus`,
 * `isReadinessRiskBucket`, `hasPerSlotSwapCoverage`), and the rich
 * `ReadinessStatus` / `ReadinessRiskBucket` / `FocusReadinessCellReport`
 * type tower. Those were a parallel implementation of catalog-coverage
 * detection authored on `feat/focus-coverage-readiness`. At the
 * 2026-05-05 merge they lost the canonicality contest to
 * `focusCoverageAudit` (which had a snapshot test, a markdown report,
 * and walked the user-facing 5-tier `SkillLevel`). The remediation API
 * was never wired up ‚Äî no script, plan, or surface ever consumed gap
 * cards or activation manifests. The audit logic was tested only by
 * its own self-test. `D138` resolved this in favor of the live
 * `focusCoverageAudit` and pruned the dead surface; only the
 * dimensions consumed by `generatedPlanDiagnostics` remain.
 *
 * If a future need surfaces for a remediation tracker (gap cards,
 * activation manifests, status workflows like
 * `failing ‚Üí source_candidate ‚Üí fixed_pending_verification ‚Üí verified`),
 * re-author it from the new context rather than restoring this file's
 * previous contents ‚Äî those types were speculative and never met
 * production. Likely homes: a JSON gap registry under `docs/`, or a
 * new typed module composed against whichever live diagnostic the
 * tracker is targeting.
 *
 * Keep this file compatible with `generatedPlanDiagnostics.ts`'s
 * imports.
 */

